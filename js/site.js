var Back = {
    w: $(window).width(),
    h: $(window).height(),
    mapa: 0,
    eventos: 0,
    contato: 0,
    refresh: function() {
        Back.mapa = $('.mapa').height();
        Back.eventos = $('.eventos').offset().top - 150;
        Back.contato = $('.contato').offset().top - 150;
    },
    init: function() {
        var l = Back.w - 40;
        var t = Back.h - 40;
        
        
        $('.back').css({
            'left': l,
            'top': t
        });
        
        $(window).scroll(function () {
            
            
            if($(this).scrollTop() > 400) 
            {
                $('.back').fadeIn();
            } 
            else 
            {
                $('.back').fadeOut();
            }
            
            $('#m1').removeClass('active');
            $('#m2').removeClass('active');
            $('#m3').removeClass('active');
            
            if($(this).scrollTop() >= 0 && $(this).scrollTop() < Back.eventos)
            {
                $('#m1').addClass('active');
            }
            else if($(this).scrollTop() >= Back.eventos && $(this).scrollTop() < Back.contato)
            {
                $('#m2').addClass('active');
            }
            else if($(this).scrollTop() >= Back.contato)
            {
                $('#m3').addClass('active');
            }
            
        });
        
        $('.back a').click(function () {
            $('body,html').stop().animate({
                    scrollTop: 0
            }, 800);
            return false;
        });
    }
    
};

